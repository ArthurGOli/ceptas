package com.ceptas.model;

/**
 *
 * @author DESENV
 */
public class Animal {
    
    private int id;
    private String especie;
    private String nomePopular;
    private String grupoTaxonomico;
    private String sexo;
    private String idade;
    private String marcacao;
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEspecie() {
        return especie;
    }

    public void setEspecie(String especie) {
        this.especie = especie;
    }

    public String getNomePopular() {
        return nomePopular;
    }

    public void setNomePopular(String nomePopular) {
        this.nomePopular = nomePopular;
    }

    public String getGrupoTaxonomico() {
        return grupoTaxonomico;
    }

    public void setGrupoTaxonomico(String grupoTaxonomico) {
        this.grupoTaxonomico = grupoTaxonomico;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getIdade() {
        return idade;
    }

    public void setIdade(String idade) {
        this.idade = idade;
    }

    public String getMarcacao() {
        return marcacao;
    }

    public void setMarcacao(String marcacao) {
        this.marcacao = marcacao;
    }
    
    
}
